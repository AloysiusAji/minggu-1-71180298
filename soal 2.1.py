'''Menghitung rata-rata dari 3 bilangan bulat sembarang.'''

print("Menghitung rata-rata dari 3 bilangan bulat sembarang")
print("ex: 1,2,3,4,5")

bil1 = int(input("Masukan bilangan bulat ke-1: "))
bil2 = int(input("Masukan bilangan bulat ke-2: "))
bil3 = int(input("Masukan bilangan bulat ke-3: "))

mean = (bil1 + bil2 + bil3) / 3

print("Hasil rata-rata 3 bilangan bulat adalah: ",mean)